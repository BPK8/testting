function countUp(el, target){
  let current = 0;
  const step = target / 40;

  const interval = setInterval(()=>{
    current += step;
    if(current >= target){
      current = target;
      clearInterval(interval);
    }
    el.innerText = current.toFixed(1);
  }, 30);
}

