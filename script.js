function revealCheck(){
  document.querySelectorAll(".card").forEach(card=>{
    card.classList.add("reveal");
    const rect = card.getBoundingClientRect();
    if(rect.top < window.innerHeight - 60){
      card.classList.add("show");
    }
  });
}

